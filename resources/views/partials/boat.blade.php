        <h2>Jeanneau<br>Sun Odyssey 36.2<br>from 1996</h2>
        <p>Zaprojektowany przez Jacques Fauroux Jeanneau Sun Odyssey 36.2 jest jachtem żaglowym o długości 36 stóp. Trzy przestronne kabiny (dwie symetryczne na rufie i jedna na dziobie) zapewniają dość miejsca by 6 osób mogło komfortowo pływać lub 4 osoby mieszkać na stałe. Przestronna mesa z podłużną kuchnią ułatwia przygotowywanie posiłków i wspólne spędzanie czasu.</p>
